    public static class PlaceholderFragment extends Fragment {

        private static final String ARG_SECTION_NUMBER = "section_number";

        public PlaceholderFragment() {

        }

        public static ServiceActivity.PlaceholderFragment newInstance(int sectionNumber) {
            ServiceActivity.PlaceholderFragment fragment = new ServiceActivity.PlaceholderFragment();
            Bundle args = new Bundle();
            args.putInt(ARG_SECTION_NUMBER, sectionNumber);
            fragment.setArguments(args);
            return fragment;
        }

        @Override
        public View onCreateView(LayoutInflater inflater, ViewGroup container,
                                 Bundle savedInstanceState) {
            View rootView = inflater.inflate(R.layout.activity_service, container, false);
            TextView textView = (TextView) rootView.findViewById(R.id.section_label);
            textView.setText(getString(R.string.section_format, getArguments().getInt(ARG_SECTION_NUMBER)));
            return rootView;
        }
    }

    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {

            //returning the current tabs
            switch (position) {
                case 0:
                    ServicesFragment tab1 = new ServicesFragment();
                    Bundle bundle = new Bundle();
                    //bundle.putString("UserId", UserId);
                    tab1.setArguments(bundle);
                    return tab1;
                case 1:
                    ServicesFragment tab2 = new ServicesFragment ();
                    Bundle bundle2 = new Bundle();
                    //bundle2.putString("UserId", UserId);
                    tab2.setArguments(bundle2);
                    return tab2;
                case 2:
                    ServicesFragment tab3 = new ServicesFragment ();
                    Bundle bundle3 = new Bundle();
                    //bundle2.putString("UserId", UserId);
                    tab3.setArguments(bundle3);
                    return tab3;

            }
            return null;
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Services";
                case 1:
                    return "Trending";
                case 2:
                    return "Locals";
            }
            return null;
        }
    }
